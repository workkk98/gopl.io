package popcount

// pc[i] is the population count of i.
// 8位一共有256种可能，所以pc的长度为256。每种可能的1的个数都存储在pc中
var pc [256]byte

func init() {
	for i := range pc {
		pc[i] = pc[i/2] + byte(i&1)
	}
}

// PopCount returns the population count (number of set bits) of x.
func PopCount(x uint64) int {
	// 统计0-8位，8-16位，16-24位，24-32位，32-40位，40-48位，48-56位，56-64位的1的个数
	return int(pc[byte(x>>(0*8))] +
		pc[byte(x>>(1*8))] +
		pc[byte(x>>(2*8))] +
		pc[byte(x>>(3*8))] +
		pc[byte(x>>(4*8))] +
		pc[byte(x>>(5*8))] +
		pc[byte(x>>(6*8))] +
		pc[byte(x>>(7*8))])
}

